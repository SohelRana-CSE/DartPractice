  smallerOrEqual = x <= y;
  greaterOrEqual = x >= y;
  notEqual = x != y;

  print(smaller);
  print(greater);
  print(equal);
  print(smallerOrEqual);
  print(greaterOrEqual);
  print(notEqual);

}